import { pinyin } from "pinyin-pro";
import { applyNeutralToneOverrides } from "@/lib/neutralTones";

export interface ToneChar {
  char: string;
  tone: 1 | 2 | 3 | 4 | 0;
  pinyin: string;
  wordStart?: boolean;
}

export type PhraseLevel = "Absolute Beginner" | "Beginner" | "Intermediate" | "Advanced";

export interface Phrase {
  characters: ToneChar[];
  english: string;
  level: PhraseLevel;
}

interface CompactPhrase {
  words: string;
  english: string;
  level: PhraseLevel;
}

const PUNCTUATION = new Set(["，", "。", "！", "？", "、", "；", "：", "…", "—"]);

function extractTone(py: string): 1 | 2 | 3 | 4 | 0 {
  if (/[āēīōūǖ]/.test(py)) return 1;
  if (/[áéíóúǘ]/.test(py)) return 2;
  if (/[ǎěǐǒǔǚ]/.test(py)) return 3;
  if (/[àèìòùǜ]/.test(py)) return 4;
  return 0;
}

export function toToneChars(words: string): ToneChar[] {
  const result: ToneChar[] = [];
  const tokens = words.split(" ");
  for (const token of tokens) {
    if (!token) continue;
    if (PUNCTUATION.has(token)) {
      result.push({ char: token, tone: 0, pinyin: "" });
      continue;
    }
    const chars = Array.from(token);
    const rawPinyinList = pinyin(token, { toneType: "symbol", type: "array" }) as string[];
    const pinyinList = applyNeutralToneOverrides(token, rawPinyinList);
    for (let i = 0; i < chars.length; i++) {
      const char = chars[i];
      let py = pinyinList[i] ?? "";
      let tone = extractTone(py);
      if (char === "一") { tone = 1; py = "yī"; }
      else if (char === "不") { tone = 4; py = "bù"; }
      const tc: ToneChar = { char, tone, pinyin: py };
      if (i > 0) tc.wordStart = false;
      result.push(tc);
    }
  }
  return result;
}

